/**
 * XKCD data class for GSON mapping
 */
public class XKCDData {


    private int num;
    private String safe_title;
    private String img;
    private String year;


    public int getNum() {
        return num;
    }

    public String getSafe_title() {
        return safe_title;
    }

    public String getImg() {
        return img;
    }

    public String getYear() {
        return year;
    }
}
